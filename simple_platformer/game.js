const canvas = document.getElementById("canvas");
const ctx = canvas.getContext("2d");

const player = {
	x: 5,
	y: 5
};

const renderer = {
	w: 16,
	h: 16
};

canvas.width = renderer.w * 32;
canvas.height = renderer.h * 32;

setInterval(() => {
	ctx.clearRect(0, 0, canvas.width, canvas.height);
	for (let j = 0; j < map.length; j++) {
		for (let i = 0; i < map[j].length; i++) {
			if (map[j][i]) {
				ctx.fillStyle = "black";
			} else {
				ctx.fillStyle = "white";
			}
			ctx.fillRect(i * 32 - (player.x * 32), j * 32 - (player.y * 32), 32, 32);
		}
	}
	ctx.fillStyle = "red";
	ctx.fillRect(renderer.w / 2 * 32, renderer.h / 2 * 32, 32, 32);

	// checking keyboard inputs
	if (touches.up) {
		player.y--;
	}
	if (touches.down) {
		player.y++;
	}
	if (touches.left) {
		player.x--;
	}
	if (touches.right) {
		player.x++;
	}
}, 40);